class ZH_INT:
    # 构造方法：接收传入的字符串/数字内容，存入实例属性
    def __init__(self, num):
        self.num = num

    # 重写 __int__ 魔法方法：调用 int(对象) 时会自动执行本方法，返回转换后的整数
    def __int__(self):
        try:
            # 尝试直接把传入内容强转成数字，如果是纯阿拉伯数字字符串直接返回
            return int(self.num)
        except ValueError:
            # 强转失败说明包含中文数字，进入中文转阿拉伯数字逻辑
            # 简体中文数字、大写财务中文数字 映射对照表
            zh = {
                "零": 0, "一": 1, "二": 2, "三": 3, "四": 4, "五": 5, "六": 6, "七": 7, "八": 8, "九": 9,
                "壹": 1, "贰": 2, "叁": 3, "肆": 4, "伍": 5, "陆": 6, "柒": 7, "捌": 8, "玖": 9,
            }
            result = 0
            # 遍历输入字符串里每一个字符
            for each in self.num:
                if each in zh:
                    # 如果是中文数字，取出对应数值累加到结果
                    result += zh[each]
                else:
                    # 如果是阿拉伯数字字符，转int后累加
                    result += int(each)
                # 每处理完一位整体乘10，留出下一位的个位空位
                result *= 10
            # 循环结束后最后多乘了一次10，整除10抵消多余进位
            return result // 10
